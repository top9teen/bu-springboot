package com.it.app.model;

public class CriterionModel {
	
	private String criterionDetail;
	private String criterionEnd;
	private String criterionStart;
	private String criterionColor;
	private String inspectionId;

	public String getCriterionDetail() {
		return criterionDetail;
	}

	public void setCriterionDetail(String criterionDetail) {
		this.criterionDetail = criterionDetail;
	}

	public String getCriterionEnd() {
		return criterionEnd;
	}

	public void setCriterionEnd(String criterionEnd) {
		this.criterionEnd = criterionEnd;
	}

	public String getCriterionStart() {
		return criterionStart;
	}

	public void setCriterionStart(String criterionStart) {
		this.criterionStart = criterionStart;
	}

	public String getCriterionColor() {
		return criterionColor;
	}

	public void setCriterionColor(String criterionColor) {
		this.criterionColor = criterionColor;
	}

	public String getInspectionId() {
		return inspectionId;
	}

	public void setInspectionId(String inspectionId) {
		this.inspectionId = inspectionId;
	}

}
